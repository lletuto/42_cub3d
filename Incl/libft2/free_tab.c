#include "libft.h"

void	free_tab(char ***tab)
{
	int	i;

	i = 0;
	if (!(*tab) || !tab)
		return ;
	while ((*tab)[i])
	{
		free((*tab)[i]);
		(*tab)[i] = NULL;
		i++;
	}
	free(*tab);
	*tab = NULL;
}
